function h = ComputeDiameter(x,y)

a = ((y(2)-y(1))*(y(3)*y(3)-y(1)*y(1)+x(3)*x(3)-x(1)*x(1))-(y(3)-y(1))*(y(2)*y(2)-y(1)*y(1)+x(2)*x(2)-x(1)*x(1)))/(2*((x(3)-x(1))*(y(2)-y(1))-(x(2)-x(1))*(y(3)-y(1))));
b = ((x(2)-x(1))*(x(3)*x(3)-x(1)*x(1)+y(3)*y(3)-y(1)*y(1))-(x(3)-x(1))*(x(2)*x(2)-x(1)*x(1)+y(2)*y(2)-y(1)*y(1)))/(2*((y(3)-y(1))*(x(2)-x(1))-(y(2)-y(1))*(x(3)-x(1))));
h = 2*sqrt((a-x(1))^2+(b-y(1))^2);

end